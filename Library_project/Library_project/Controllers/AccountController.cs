        // POST: /Account/Login
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Login(LoginViewModel model, string returnUrl)
        {
            if (!ModelState.IsValid)
            {
                return View(model);
            }

            // This doesn't count login failures towards account lockout
            // To enable password failures to trigger account lockout, change to shouldLockout: true
            var result = await SignInManager.PasswordSignInAsync(model.Email, model.Password, model.RememberMe, shouldLockout: false);
            switch (result)
            {
                case SignInStatus.Success:
        
                    var UserId = await UserManager.FindByEmailAsync(model.Email);
                    var Role = await UserManager.GetRolesAsync(UserId.Id.ToString());
                    Session.Add("Role", Role[0]);
                    Session.Add("UserID", UserId.Id);
                    ViewBag.Role = Role[0];
                    if (Role[0] == "BasicAdmin")
                    {
                        if(Checkvalid())
                           return RedirectToAction("Index", "BasicAdmin");
                        else
                            return RedirectToAction("ChangePassword", "Manage");
                    }
                    else if (Role[0] == "Admin")
                    {
                        if (Checkvalid())
                            return RedirectToAction("Index", "Admin");
                        else
                            return RedirectToAction("ChangePassword", "Manage");
                    }
                    else if (Role[0] == "Employee")
                    {
                        if (Checkvalid())
                            return RedirectToAction("Index", "Employee");
                        else
                            return RedirectToAction("ChangePassword", "Manage");
                    }
                    else if (Role[0] == "Member")
                    {
                        if (Checkvalid())
                            return RedirectToAction("Index", "Member");
                        else
                            return RedirectToAction("WaitValidation", "Member");
                    }
                    return RedirectToLocal(returnUrl);
                case SignInStatus.LockedOut:
                    return View("Lockout");
                case SignInStatus.RequiresVerification:
                    return RedirectToAction("SendCode", new { ReturnUrl = returnUrl, RememberMe = model.RememberMe });
                case SignInStatus.Failure:
                default:
                    ModelState.AddModelError("", "Invalid login attempt.");
                    return View(model);
            }
        }

        //check validation
        public bool Checkvalid()
        {
            var UserID = Session["UserID"].ToString();
            var user=UserManager.FindById(UserID);

            if (user.IsValid==true)
            {
                return true;
            }
            else
            {
                return false;
            }
                
        }

