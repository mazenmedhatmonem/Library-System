        public async Task<ActionResult> Delete(string id)
        {

            ApplicationDbContext db = new ApplicationDbContext();

            var user = UserManager.FindById(id);
            var userRole = UserManager.GetRoles(user.Id);
            var logins = user.Logins;
            var worker = db.Worker.Where(a => a.WorkerID == id).SingleOrDefault();
            if (worker != null)
            {
                db.Worker.Remove(worker);
            }
            else
            {
                var member = db.Member.Where(a => a.MemberID == id).SingleOrDefault();
                if (User.IsInRole("BasicAdmin"))
                {
                    Nodification nodify = new Nodification();
                    nodify.Role = "Employee";
                    nodify.Message = "Basic Admin Delete an member with id=" + user.Id;
                    db.Nodification.Add(nodify);
                }
                else if (User.IsInRole("Admin"))
                {
                    Nodification nodify = new Nodification();
                    nodify.Role = "Employee";
                    nodify.Message = "Admin Delete an member with id=" + user.Id;
                    db.Nodification.Add(nodify);
                }
                db.Member.Remove(member);
            }
            db.SaveChanges();
            // Delete every login, if he has
            foreach (var login in logins.ToList())
            {
                await UserManager.RemoveLoginAsync(login.UserId, new UserLoginInfo(login.LoginProvider, login.ProviderKey));
            }

            // Delete every role, if he has
            var rolesForUser = await UserManager.GetRolesAsync(id);
            if (rolesForUser.Count() > 0)
            {
                foreach (var item in rolesForUser.ToList())
                {
                    // item should be the name of the role
                    var result = await UserManager.RemoveFromRoleAsync(user.Id, item);
                }
            }

            // Delete the user itself
            await UserManager.DeleteAsync(user);
            //check ana meen Basic admin wla admin wala Employee //session
            if (User.IsInRole("BasicAdmin"))
            {
                if (userRole[0] == "Admin")
                    return View("~/Views/BasicAdmin/ShowAdmin.cshtml", db.Worker.Where(a => a.UserType == "Admin").ToList());
                else if (userRole[0] == "Employee")
                    return View("~/Views/Admin/ShowEmployee.cshtml", db.Worker.Where(a => a.UserType == "Employee").ToList());
                else if (userRole[0] == "Member")
                    return View("~/Views/Employee/ReturnMembers.cshtml", db.Member.ToList());
                else
                    return View("~/Views/BasicAdmin/Index");
            }
            else if (User.IsInRole("Admin"))
            {
                if (userRole[0] == "Employee")
                    return View("~/Views/Admin/ShowEmployee.cshtml", db.Worker.Where(a => a.UserType == "Employee").ToList());
                else if (userRole[0] == "Member")
                    return View("~/Views/Employee/ReturnMembers.cshtml", db.Member.ToList());
                else
                    return View("~/Views/Admin/Index");
            }
            else if (User.IsInRole("Employee"))
            {
                return View("~/Views/Employee/ReturnMembers.cshtml", db.Member.ToList());
            }
            else
                return View();

        }

